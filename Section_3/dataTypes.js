
// Primitive DataTypes: their values cannot be changed
// Numbers 
let age = 22;

// String
let statement = "Hello World";

// Boolean 
let isGraduated = true;

// Undefined
let a = undefined;

// null 
let value = null;

// Symbol 
const uniqueSymbol = Symbol('description');

// Non-Primitive DataTypes: mutable and can be modified. 
// Object 
let userDetails = {
    name: "Ammar",
    age: 22,
    isGraduated: true,
};

// Array:  Ordered list of value
let count = ['Hello', '22', true];

// Function
function fun(x, y) {
    return x + y;
}
// Arrow Function 
const multiply = (a, b) => a * b;

// Date 
const today = new Date();
